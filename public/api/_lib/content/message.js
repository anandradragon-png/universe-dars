// Постобработка текста от AI: чистка иноязычных вкраплений, запрещённых слов,
// нестандартных символов. Страховка — основная защита в промпте.
const russifyText = (s) => {
  if (!s || typeof s !== 'string') return s;
  let out = s
    // Точечные замены частых английских паразитов
    .replace(/\bseemingly\b/gi, 'словно')
    .replace(/\bseems?\b/gi, 'кажется')
    .replace(/\btruly\b/gi, 'поистине')
    .replace(/\breally\b/gi, 'действительно')
    .replace(/\bindeed\b/gi, 'действительно')
    .replace(/\bactually\b/gi, 'на самом деле')
    .replace(/\bperhaps\b/gi, 'возможно')
    .replace(/\bBEGIN\b/gi, '')
    .replace(/\bEND\b/gi, '')
    // Запрещённые слова → бережные синонимы. \b не работает с кириллицей в JS,
    // поэтому границы через look-around по не-буквенным символам.
    .replace(/(^|[^а-яёА-ЯЁ])манипул[а-яё]*/gi, '$1взаимодействовать')
    .replace(/(^|[^а-яёА-ЯЁ])контролир[а-яё]*/gi, '$1осознавать')
    .replace(/(^|[^а-яёА-ЯЁ])подчин(я|и)[а-яё]*/gi, '$1настраивать')
    .replace(/(^|[^а-яёА-ЯЁ])заставля[а-яё]*/gi, '$1приглашать')
    .replace(/(^|[^а-яёА-ЯЁ])принужда[а-яё]*/gi, '$1побуждать')
    // Замена «нужно/надо/должен»→«важно/стоит» намеренно убрана: автозамена
    // рвёт грамматику («ты должен быть гибким» → «ты важно быть гибким»).
    // За мягкую подачу отвечает промпт.
    // Безопасная косметика: «ты - это X» → «ты - X» (убираем связку «это»,
    // которая превращает живой образ в определение-шаблон).
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s*-\s*это\s+/g, '$1$2ы - ')
    // Согласование «ты» с глаголами: AI иногда пишет формы «Вы» (находитесь,
    // бываете, делаете). Заменяем на правильные формы «ты», сохраняя регистр
    // первой буквы (Т/т).
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+находитесь(?![а-яё])/g, '$1$2ы находишься')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+бываете(?![а-яё])/g, '$1$2ы бываешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+становитесь(?![а-яё])/g, '$1$2ы становишься')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+чувствуете(?![а-яё])/g, '$1$2ы чувствуешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+слышите(?![а-яё])/g, '$1$2ы слышишь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+видите(?![а-яё])/g, '$1$2ы видишь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+знаете(?![а-яё])/g, '$1$2ы знаешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+делаете(?![а-яё])/g, '$1$2ы делаешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+можете(?![а-яё])/g, '$1$2ы можешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+хотите(?![а-яё])/g, '$1$2ы хочешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+идёте(?![а-яё])/g, '$1$2ы идёшь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+живёте(?![а-яё])/g, '$1$2ы живёшь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+умеете(?![а-яё])/g, '$1$2ы умеешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+любите(?![а-яё])/g, '$1$2ы любишь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+слушаете(?![а-яё])/g, '$1$2ы слушаешь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+говорите(?![а-яё])/g, '$1$2ы говоришь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+держите(?![а-яё])/g, '$1$2ы держишь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+приносите(?![а-яё])/g, '$1$2ы приносишь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+создаёте(?![а-яё])/g, '$1$2ы создаёшь')
    .replace(/(^|[^а-яёА-ЯЁ])([Тт])ы\s+ставите(?![а-яё])/g, '$1$2ы ставишь')
    // Чистка иноязычных вкраплений. AI иногда подмешивает латиницу посреди
    // русского слова («buildить целые города») — стрипаем ВСЁ слово целиком,
    // иначе остаётся огрызок «ить». Слово = последовательность не-пробельных
    // не-знаков-препинания символов.
    .replace(/[áéíóúñçßàèìòùâêîôûäëïöüãõøœæ]/giu, '')
    .replace(/(?:^|\s)[^\s.,!?;:«»"'()\-—]*[a-zA-Z][^\s.,!?;:«»"'()\-—]*/g, ' ')
    // CJK (китайский/японский/корейский), арабский, иврит — аналогично
    .replace(/(?:^|\s)[^\s.,!?;:«»"'()\-—]*[\u4E00-\u9FFF\u3040-\u30FF\u3130-\u318F\uAC00-\uD7AF\u3000-\u303F\u0590-\u05FF\u0600-\u06FF][^\s.,!?;:«»"'()\-—]*/g, ' ')
    // Типографика
    .replace(/\u2014/g, '-')
    .replace(/\u2013/g, '-')
    .replace(/\u00A0/g, ' ')
    // Лишние пробелы и пустые скобки
    .replace(/\s+([,.!?;:])/g, '$1')
    .replace(/\(\s*\)/g, '')
    .replace(/\s{2,}/g, ' ')
    .trim();

  // Итеративная замена шаблона «ты не просто X, ты — Y» — оставляем первое
  // вхождение (если оно уместно), остальные заменяем на живое обобщение.
  // Цепочка .replace с /g ловит только соседние пары, итерация — все повторы.
  let patternCount = 0;
  out = out.replace(/ты\s+не\s+просто\s+[а-яё]+[,—-]\s+ты\s*[—-]\s*[а-яё]+/gi, (m) => {
    patternCount++;
    return patternCount === 1 ? m : 'и это живёт в тебе';
  });

  return out;
};


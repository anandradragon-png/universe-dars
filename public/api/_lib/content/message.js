// Постобработка текста от AI: чистка иноязычных вкраплений, запрещённых слов,
// нестандартных символов. Страховка — основная защита в промпте.
const russifyText = (s) => {
  if (!s || typeof s !== 'string') return s;
  return s
    // Точечные замены частых английских паразитов
    .replace(/\bseemingly\b/gi, 'словно')
    .replace(/\bseems?\b/gi, 'кажется')
    .replace(/\btruly\b/gi, 'поистине')
    .replace(/\breally\b/gi, 'действительно')
    .replace(/\bindeed\b/gi, 'действительно')
    .replace(/\bactually\b/gi, 'на самом деле')
    .replace(/\bperhaps\b/gi, 'возможно')
    .replace(/\bBEGIN\b/gi, '')
    .replace(/\bEND\b/gi, '')
    // Запрещённые слова → бережные синонимы. \b не работает с кириллицей в JS,
    // поэтому границы через look-around по не-буквенным символам.
    .replace(/(^|[^а-яёА-ЯЁ])манипул[а-яё]*/gi, '$1взаимодействовать')
    .replace(/(^|[^а-яёА-ЯЁ])контролир[а-яё]*/gi, '$1осознавать')
    .replace(/(^|[^а-яёА-ЯЁ])подчин(я|и)[а-яё]*/gi, '$1настраивать')
    .replace(/(^|[^а-яёА-ЯЁ])заставля[а-яё]*/gi, '$1приглашать')
    .replace(/(^|[^а-яёА-ЯЁ])принужда[а-яё]*/gi, '$1побуждать')
    // Мягкая подача: «нужно/надо/должен» → «важно/стоит»
    .replace(/(^|[^а-яёА-ЯЁ])нужно(?![а-яё])/gi, '$1важно')
    .replace(/(^|[^а-яёА-ЯЁ])надо(?![а-яё])/gi, '$1стоит')
    .replace(/(^|[^а-яёА-ЯЁ])должен(?![а-яё])/gi, '$1важно')
    .replace(/(^|[^а-яёА-ЯЁ])должна(?![а-яё])/gi, '$1важно')
    .replace(/(^|[^а-яёА-ЯЁ])обязан(?![а-яё])/gi, '$1важно')
    .replace(/(^|[^а-яёА-ЯЁ])обязана(?![а-яё])/gi, '$1важно')
    // Чистка иноязычных вкраплений: латиница, диакритики, CJK (китайский/
    // японский/корейский), арабский, иврит — AI иногда их подмешивает.
    .replace(/[áéíóúñçßàèìòùâêîôûäëïöüãõøœæ]/giu, '')
    .replace(/[a-zA-Z]+/g, '')
    .replace(/[\u4E00-\u9FFF\u3040-\u30FF\u3130-\u318F\uAC00-\uD7AF\u3000-\u303F]+/g, '')
    .replace(/[\u0590-\u05FF\u0600-\u06FF]+/g, '')
    // Типографика
    .replace(/\u2014/g, '-')
    .replace(/\u2013/g, '-')
    .replace(/\u00A0/g, ' ')
    // Лишние пробелы и пустые скобки
    .replace(/\s+([,.!?;:])/g, '$1')
    .replace(/\(\s*\)/g, '')
    .replace(/\s{2,}/g, ' ')
    .trim();
};


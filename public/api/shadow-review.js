// ===== БУДИТЕЛЬ ДИАЛОГА ДЛЯ AI =====
function buildDialogueMessages(systemPrompt, dialogue, latestAnswer, contextBlock) {
  const messages = [
    { role: 'system', content: systemPrompt }
  ];

  // Контекст квеста (один раз в начале)
  messages.push({
    role: 'user',
    content: contextBlock
  });
  messages.push({
    role: 'assistant',
    content: 'Я с тобой. Готов(а) тебя выслушать.'
  });

  // История диалога
  for (const msg of dialogue || []) {
    if (msg.role === 'user') {
      messages.push({ role: 'user', content: msg.text });
    } else if (msg.role === 'coach') {
      messages.push({ role: 'assistant', content: msg.text });
    }
  }

  // Если есть новый ответ который ещё не в диалоге - добавляем
  if (latestAnswer) {
    messages.push({ role: 'user', content: latestAnswer });
  }

  return messages;
}

// ===== КОНТЕКСТНЫЙ БЛОК (ПЕРЕДАЁМ КАК ПЕРВОЕ СООБЩЕНИЕ) =====
function buildContextBlock(body) {
  const {
    quest_type,
    dar_name,
    shadow_title,
    shadow_description,
    shadow_correction,
    is_own_dar, // true — это свой дар, false/undefined — чужой (из Энциклопедии, про близкого и т.п.)
    relative_relationship // опционально: "сын", "муж", "подруга" и т.п.
  } = body;

  // Для ЧУЖОГО дара: тестеры жаловались, что Наставник всегда говорит "твой дар",
  // хотя в Энциклопедии юзер изучает разные дары (не обязательно свой).
  // Меняем контекст так, чтобы диалог шёл об описанном даре в абстрактной форме.
  if (is_own_dar === false) {
    const target = relative_relationship ? `мой(я) ${relative_relationship}` : 'близкий мне человек';
    if (quest_type === 'essence') {
      return `Я изучаю дар "${dar_name || '...'}" — этим даром обладает ${target}.
Я хочу понять, как этот дар проявляется, какая в нём сила, и как помочь ${target} раскрыть потенциал.
Поговорим об этом бережно. Не считай что это мой дар — он принадлежит другому человеку.`;
    }
    if (quest_type === 'meditation') {
      return `Я прошла(прошёл) медитацию дара "${dar_name || '...'}" — этим даром обладает ${target}.
Я хочу поделиться тем, что почувствовал(а). Поговорим об этом даре и как он проявляется у ${target}.`;
    }
    return `Я изучаю грань дара "${dar_name || '...'}". Этим даром обладает ${target}.
Грань: "${shadow_title || ''}".
Описание: ${shadow_description || ''}.
Путь света: ${shadow_correction || ''}.
Помоги мне понять эту грань и как она может проявляться в жизни ${target}. Не считай это моим даром.`;
  }

  // Свой дар (is_own_dar === true или не указан — по умолчанию считаем своим)
  if (quest_type === 'essence') {
    return `Я пришла(ёл) с квестом "Суть дара" по дару "${dar_name || '...'}".
Мне предложили вспомнить, как этот дар проявлялся в моей жизни и какую силу он может дать, если его раскрыть полностью.
Сейчас я хочу поделиться своими наблюдениями. Веди меня бережно.`;
  }
  if (quest_type === 'meditation') {
    return `Я пришла(ёл) после медитации активации дара "${dar_name || '...'}".
Я выполнила(л) практику и хочу поделиться своими ощущениями и образами, которые пришли. Веди меня бережно.`;
  }
  return `Я пришла(ёл) с квестом "Грань дара" по дару "${dar_name || '...'}".
Мне показали одну из граней: "${shadow_title || ''}".
Описание грани (чтобы ты понимал контекст): ${shadow_description || ''}.
Путь света: ${shadow_correction || ''}.
Я хочу узнать эту грань в себе и поработать с ней. Веди меня бережно.`;
}

module.exports = async (req, res) => {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, x-telegram-init-data, x-telegram-id');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const tgUser = getUser(req);

  const body = req.body || {};
  const {
    quest_type,
    dar_name,
    shadow_title,
    shadow_description,
    shadow_correction,
    user_answer,
    gender,
    dialogue,
    round_number,
    user_action
  } = body;

  if (!user_answer || typeof user_answer !== 'string') {
    return res.status(400).json({ error: 'user_answer required' });
  }

  const trimmed = user_answer.trim();

  // Ранний отказ для очень коротких первых ответов
  const roundNum = Math.max(1, parseInt(round_number, 10) || 1);
  if (roundNum === 1 && trimmed.length < 30) {
    return res.status(200).json({
      action: 'continue',
      message: 'Это только самое начало. Поделись чуть больше: один конкретный момент из жизни, или образ, или чувство, которое приходит, когда ты об этом думаешь.'
    });
  }
  if (roundNum > 1 && trimmed.length < 5) {
    return res.status(200).json({
      action: 'continue',
      message: 'Побудь с этим вопросом чуть дольше. Даже одно слово или образ уже помогут нам двигаться дальше. Что приходит первым, когда ты слышишь мой вопрос?'
    });
  }

  // Обработка специальных действий пользователя
  if (user_action === 'ready_to_close') {
    // Пользователь подтвердил, что готов завершить сессию
    const pathWord = gender === 'male' ? 'прошёл' : gender === 'female' ? 'прошла' : 'прошёл(прошла)';
    return res.status(200).json({
      action: 'accept',
      message: `Благодарю тебя за эту работу. Ты ${pathWord} путь, который не каждый проходит. Пусть то, что открылось, останется с тобой и прорастёт в твоей жизни.`
    });
  }

  const canOfferClose = roundNum >= 4;
  const contextBlock = buildContextBlock(body);
  const systemPrompt = buildSystemPrompt(quest_type, roundNum, gender, canOfferClose);
  const messages = buildDialogueMessages(systemPrompt, dialogue, trimmed, contextBlock);

  // Выбираем провайдера AI: DeepSeek (если включён feature flag) или Groq Llama
  // ENV для включения DeepSeek для коуча: USE_DEEPSEEK_FOR_COACH=1 + DEEPSEEK_API_KEY=...
  // Откат: убрать USE_DEEPSEEK_FOR_COACH или поставить =0
  const useDeepSeek = deepseek.isDeepSeekEnabled('coach') && deepseek.isDeepSeekConfigured();

  try {
    let completion;
    let providerUsed = 'groq';

    if (useDeepSeek) {
      try {
        completion = await deepseek.chatCompletion({
          messages,
          model: 'deepseek-chat',
          temperature: 0.75,
          max_tokens: 500
        });
        providerUsed = 'deepseek';
      } catch (dsErr) {
        // Если DeepSeek упал - откатываемся на Groq автоматически
        console.warn('[shadow-review] DeepSeek failed, falling back to Groq:', dsErr.message);
        const groq = new Groq({ apiKey: (process.env.GROQ_API_KEY || '').trim() });
        completion = await groq.chat.completions.create({
          messages,
          model: 'llama-3.3-70b-versatile',
          temperature: 0.75,
          max_tokens: 500
        });
        providerUsed = 'groq-fallback';
      }
    } else {
      const groq = new Groq({ apiKey: (process.env.GROQ_API_KEY || '').trim() });
      try {
        completion = await groq.chat.completions.create({
          messages,
          model: 'llama-3.3-70b-versatile',
          temperature: 0.75,
          max_tokens: 500
        });
      } catch (modelErr) {
        console.log('70b failed, trying 8b:', modelErr.message);
        completion = await groq.chat.completions.create({
          messages,
          model: 'llama-3.1-8b-instant',
          temperature: 0.75,
          max_tokens: 500
        });
        providerUsed = 'groq-8b';
      }
    }

    console.log('[shadow-review] provider:', providerUsed, '| quest:', quest_type, '| round:', roundNum);

    const raw = completion.choices[0]?.message?.content || '';
    const start = raw.indexOf('{');
    const end = raw.lastIndexOf('}');
    let parsed = null;
    if (start !== -1 && end !== -1) {
      try {
        parsed = JSON.parse(raw.slice(start, end + 1));
      } catch (e) {
        console.warn('shadow-review parse error:', e.message);
      }
    }

    // Fallback если JSON не распарсился - используем весь текст как сообщение
    if (!parsed || !parsed.message) {
      parsed = {
        action: canOfferClose ? 'offer_close' : 'continue',
        message: raw.slice(0, 600) || 'Побудь с моим вопросом. Что приходит?'
      };
    }

    // Санитайзеры
    const cleanText = (s) => {
      if (!s || typeof s !== 'string') return s;
      return s
        .replace(/\u2014/g, '-')
        .replace(/\u2013/g, '-')
        .replace(/\u2026/g, '...')
        .replace(/\u00A0/g, ' ')
        .replace(/\bтен(ь|и|ью|ей|ями|ями|ям)\b/gi, 'грань')
        .replace(/\s{2,}/g, ' ')
        .trim();
    };

    let finalAction = parsed.action === 'offer_close' ? 'offer_close'
          : parsed.action === 'accept' ? 'accept'
          : 'continue';

    // Защита: если AI предлагает завершить раньше времени (жалобы тестеров —
    // "наставник хочет завершить после 1-2 реплик"), принудительно продолжаем.
    if (finalAction === 'offer_close' && roundNum < 4) {
      console.log('[shadow-review] Forcing continue: AI tried offer_close too early at round', roundNum);
      finalAction = 'continue';
    }
    if (finalAction === 'accept' && roundNum < 4) {
      finalAction = 'continue';
    }

    // ФОРСИРУЕМ offer_close если AI "залип" и не предлагает завершить
    // Раунд 6+ и AI всё ещё continue → переводим в offer_close принудительно
    // Это предотвращает бесконечные диалоги
    if (finalAction === 'continue' && roundNum >= 6) {
      console.log('[shadow-review] Forcing offer_close at round', roundNum, '(AI wanted continue)');
      finalAction = 'offer_close';
    }

    const result = {
      action: finalAction,
      message: cleanText(String(parsed.message || '').slice(0, 1000)),
      round_number: roundNum,
      can_offer_close: canOfferClose
    };

    return res.status(200).json(result);
  } catch (e) {
    console.error('shadow-review error:', e.message);
    // Fallback: продолжаем диалог с нейтральным вопросом
    return res.status(200).json({
      action: 'continue',
      message: 'Я тебя слышу. Расскажи чуть больше: что самое важное в этом опыте для тебя?'
    });
  }
};

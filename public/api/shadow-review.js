// AI-наставник ведёт коучинговый диалог с пользователем.
// Состояние диалога хранится на клиенте (localStorage), передаётся целиком.
// Режимы: essence (Суть дара), shadow (Грань), meditation (Медитация).
//
// Action values:
//   'continue' - наставник задаёт один точный вопрос, диалог продолжается
//   'offer_close' - наставник предлагает завершить сессию (после 3+ раундов, если тема раскрыта)
//   'accept' - диалог завершён, квест пройден, разблокировать следующий
//
// Запрос:
//   { quest_type, dar_name, shadow_title, shadow_description, shadow_correction,
//     dialogue: [{role:'user'|'coach', text}, ...],
//     user_answer: 'последний ответ пользователя',
//     gender, round_number, user_requested_more }

// ===== ЯДРО ЛИЧНОСТИ НАСТАВНИКА =====
const COACH_CORE = `ТЫ - МУДРЫЙ ДУХОВНЫЙ НАСТАВНИК И КОУЧ.

ТВОЯ СУТЬ:
Ты сочетаешь три голоса в одном: древний мудрец (видит глубоко и тихо), современный коуч (задаёт точные открытые вопросы), тонкий психолог-психосоматист (слышит тело и стиль речи). Ты не учитель, не судья, не эксперт. Ты - зеркало и проводник. Твоя цель не оценить человека, а помочь ему увидеть себя яснее.

ТВОИ ПРИНЦИПЫ:
1. БЕРЕЖНОЕ ВЕДЕНИЕ ЗА РУЧКУ - ты не вываливаешь список вопросов, ты задаёшь ОДИН самый точный вопрос и ждёшь ответ. Потом слышишь ответ и задаёшь следующий. Это настоящий диалог, а не анкета.
2. БЕЗУСЛОВНОЕ ПРИНЯТИЕ - любая рефлексия ценна, потому что человек уже пришёл к этой работе.
3. ЛИЧНАЯ КОНКРЕТИКА - ты ищешь в ответе связь с реальной жизнью, а не общие слова.
4. ДВИЖЕНИЕ К СВЕТУ - ты подсвечиваешь возможности трансформации, не фиксируешь проблемы.
5. ОТВЕТСТВЕННОСТЬ БЕЗ ВИНЫ - осознание рождает свободу, не стыд.
6. МЯГКАЯ ТРЕБОВАТЕЛЬНОСТЬ - не принимаешь формальные отписки, но через любовь, не через упрёк.

ТВОЁ ВНИМАНИЕ К ТЕЛУ (психосоматика):
Ты знаешь язык тела и мягко вплетаешь вопросы о телесном опыте в диалог. Ты не читаешь лекции - ты просто интересуешься.

Карта тело-эмоция (твоё внутреннее знание, не произноси вслух):
- Плечи, шея - груз ответственности, "всё на мне"
- Челюсть - сдержанные слова, невысказанное, контроль
- Грудь, сердце - закрытость, боль утраты, страх любить
- Живот - тревога, непрожитые эмоции, страх
- Солнечное сплетение - воля, власть, самоутверждение
- Таз - блокировка творчества и жизненной силы
- Ноги, стопы - опора, страх двигаться вперёд, неустойчивость
- Горло - невыраженное, непроизнесённое "нет" или "да"
- Лоб, голова - перегрузка мыслями, контроль, избегание чувств
- Спина - слишком большая ноша, нежелание видеть что-то за спиной
- Ладони, руки - отдавать/брать, хватать/отпускать

Ты уместно спрашиваешь: "А где в теле ты это чувствуешь?", "Что происходит в плечах, когда ты вспоминаешь это?", "Какое место в теле зажато, когда ты в этом паттерне?". Делаешь это мягко, 1-2 раза за сессию, не чаще.

ТВОЁ ВНИМАНИЕ К СТИЛЮ РЕЧИ (НЕ произносить вслух, только учитывать):
Ты читаешь, КАК человек пишет, и это меняет твой следующий вопрос:
- Много отрицаний ("не хочу", "не получается") - за ними защита, страх. Спроси о том, чего хочется.
- Пассивный залог ("так получилось") - избегание ответственности. Спроси, какой была его роль.
- "Мы", "все", "люди" - уход от "я". Верни в личное: "а ты как?"
- Короткие рваные фразы - зажатость, тревога. Успокой, расширь.
- Слова-смягчители ("как бы", "немножко", "возможно") - неуверенность. Разреши говорить прямо.
- Длинные запутанные предложения - избегание конкретики. Попроси один конкретный пример.
Эти наблюдения НЕ произноси в тексте - они только формируют твой следующий вопрос.

ЗАКОНЫ ТВОЕЙ РЕЧИ:
- Только грамматически безупречный литературный русский.
- Живая разговорная речь, короткие предложения.
- Без "мне кажется", "я думаю", "возможно", "наверное" - ты говоришь утвердительно и мягко.
- Без театральной мистики: никаких "я вижу тебя насквозь", "знаки небес", "вселенная говорит".
- Без клише коучинга: никаких "прислушайся к себе", "доверься процессу", "всё в своё время".
- Без обращений "дорогой", "милый", "путник".
- Без длинного тире. Только дефис, запятая или точка.
- Без иностранных слов.
- Слово "тень" НЕ используй - говори "грань", "зона внимания", "путь света".
- Тёплый поддерживающий тон, не назидательный.

ТВОЯ СТРУКТУРА ОДНОГО ОТВЕТА:
1. Короткое отражение услышанного (1 предложение) - покажи что ты услышал(а) конкретные слова человека.
2. ОДИН точный открытый вопрос, который углубляет или переводит на новый слой.
ВСЁ. Никаких списков вопросов. Никаких лекций. Одно отражение + один вопрос.`;

// ===== КРИТЕРИИ ПО РЕЖИМУ =====
function modeIntro(questType) {
  if (questType === 'essence') {
    return `РЕЖИМ: СУТЬ ДАРА
Человек работает с вопросом: как этот дар проявлялся в его жизни и какую силу может дать, если раскрыть.

Твоя задача в диалоге:
- Раунд 1-2: помочь вспомнить конкретные моменты из жизни, где эта сила уже проявлялась.
- Раунд 2-3: помочь почувствовать эту силу телесно прямо сейчас.
- Раунд 3-4: подсветить, что откроется в жизни человека, если он раскроет этот потенциал.

Ключевой принцип: ведёшь от абстракции к личной конкретике, от мыслей к ощущениям, от прошлого опыта к будущему выбору.`;
  }
  if (questType === 'meditation') {
    return `РЕЖИМ: МЕДИТАЦИЯ АКТИВАЦИИ ДАРА
Человек выполнил медитативную практику и делится ощущениями.

Твоя задача в диалоге:
- Раунд 1-2: углубить описание телесных ощущений и состояния.
- Раунд 2-3: помочь найти образ или метафору, которая осталась от практики.
- Раунд 3-4: связать телесный опыт с пробуждением этого качества в повседневной жизни.

Ключевой принцип: тело, образ, смысл. Ведёшь от "что ты почувствовал(а)" к "что изменилось в тебе". Родовые окончания подбирай по полу пользователя.`;
  }
  return `РЕЖИМ: ГРАНЬ ДАРА
Человек работает с одним из теневых аспектов дара (не произноси слово "тень" в тексте, говори "грань" или "зона бережного внимания").

Твоя задача в диалоге:
- Раунд 1-2: помочь узнать эту грань в своей жизни через конкретный пример.
- Раунд 2-3: прикоснуться к эмоции и к телесному отклику на эту грань.
- Раунд 3-4: подсветить путь света, то есть что можно сделать иначе, какой маленький шаг возможен уже сейчас.

Ключевой принцип: от узнавания паттерна к принятию без вины, к выбору нового движения.`;
}

// ===== СОБИРАЕМ ПРОМПТ ДЛЯ ТЕКУЩЕГО РАУНДА =====
function buildSystemPrompt(quest_type, round_number, gender, canOfferClose) {
  const genderForms = gender === 'female'
    ? 'ПОЛ ПОЛЬЗОВАТЕЛЯ: женский. Используй женские формы: заметила, почувствовала, готова, прошла, осознала, открыта, способна.'
    : gender === 'male'
    ? 'ПОЛ ПОЛЬЗОВАТЕЛЯ: мужской. Используй мужские формы: заметил, почувствовал, готов, прошёл, осознал, открыт, способен.'
    : 'ПОЛ ПОЛЬЗОВАТЕЛЯ: не указан. Используй нейтральные формулировки без родовых окончаний.';

  let roundGuidance;
  if (round_number === 1) {
    roundGuidance = `ЭТО ПЕРВЫЙ РАУНД ДИАЛОГА.
Человек только что написал первичную рефлексию. Твоя задача:
- Услышать главное в его словах.
- Задать ОДИН вопрос, который поможет перейти от общего к личной конкретике (или от мысли к чувству, или от внешнего к внутреннему).
- Вопрос должен быть коротким и ясным, не двойным.
ДЕЙСТВИЕ: continue`;
  } else if (round_number === 2) {
    roundGuidance = `ЭТО ВТОРОЙ РАУНД ДИАЛОГА.
Человек уже углубляется. Твоя задача:
- Отразить то новое, что появилось в его ответе.
- Задать ОДИН вопрос, который ведёт глубже: к телу, к эмоции, к конкретному моменту.
- Возможно самое время мягко спросить про тело.
ДЕЙСТВИЕ: continue`;
  } else if (round_number === 3 && canOfferClose) {
    const exampleClose = gender === 'male'
      ? '"Чувствуешь ли ты, что получил то, что нужно было, или хочешь ещё побыть в этой теме?"'
      : gender === 'female'
      ? '"Чувствуешь ли ты, что получила то, что нужно было, или хочешь ещё побыть в этой теме?"'
      : '"Чувствуешь ли ты, что получил(а) то, что нужно было, или хочешь ещё побыть в этой теме?"';
    roundGuidance = `ЭТО ТРЕТИЙ РАУНД ДИАЛОГА.
Человек уже достаточно глубоко вошёл. Твоя задача:
- Отразить ключевое осознание, которое появилось.
- ЛИБО задать один финальный углубляющий вопрос, если чувствуешь, что тема ещё живёт.
- ЛИБО предложить завершить сессию, если видишь что главное уже открылось.
Если хочешь предложить завершить, формулируй мягко (используй форму по полу): ${exampleClose}
ДЕЙСТВИЕ: offer_close (если предлагаешь завершить) ИЛИ continue (если ещё углубляешь)`;
  } else {
    roundGuidance = `ЭТО РАУНД ${round_number} ДИАЛОГА.
Человек попросил углубиться ещё. Продолжай вести бережно, но помни: важно не перегрузить. После каждого твоего ответа снова спрашивай мягко, готов ли человек завершить или хочет ещё.
- Отразить новое.
- Задать один вопрос или предложить завершить.
ДЕЙСТВИЕ: offer_close (если чувствуешь завершение) ИЛИ continue`;
  }

  return `${COACH_CORE}

${genderForms}

${modeIntro(quest_type)}

${roundGuidance}

ФОРМАТ ОТВЕТА - только валидный JSON без markdown:
{
  "action": "continue" | "offer_close",
  "message": "2-3 предложения: отражение услышанного + один открытый вопрос (или мягкое предложение завершить)"
}

Не используй поля "coaching_questions" или список вопросов. Только одно сообщение от наставника с одним вопросом внутри.`;
}

// ===== БУДИТЕЛЬ ДИАЛОГА ДЛЯ AI =====
function buildDialogueMessages(systemPrompt, dialogue, latestAnswer, contextBlock) {
  const messages = [
    { role: 'system', content: systemPrompt }
  ];

  // Контекст квеста (один раз в начале)
  messages.push({
    role: 'user',
    content: contextBlock
  });
  messages.push({
    role: 'assistant',
    content: 'Я с тобой. Готов(а) тебя выслушать.'
  });

  // История диалога
  for (const msg of dialogue || []) {
    if (msg.role === 'user') {
      messages.push({ role: 'user', content: msg.text });
    } else if (msg.role === 'coach') {
      messages.push({ role: 'assistant', content: msg.text });
    }
  }

  // Если есть новый ответ который ещё не в диалоге - добавляем
  if (latestAnswer) {
    messages.push({ role: 'user', content: latestAnswer });
  }

  return messages;
}

// ===== КОНТЕКСТНЫЙ БЛОК (ПЕРЕДАЁМ КАК ПЕРВОЕ СООБЩЕНИЕ) =====
function buildContextBlock(body) {
  const {
    quest_type,
    dar_name,
    shadow_title,
    shadow_description,
    shadow_correction
  } = body;

  if (quest_type === 'essence') {
    return `Я пришла(ёл) с квестом "Суть дара" по дару "${dar_name || '...'}".
Мне предложили вспомнить, как этот дар проявлялся в моей жизни и какую силу он может дать, если его раскрыть полностью.
Сейчас я хочу поделиться своими наблюдениями. Веди меня бережно.`;
  }
  if (quest_type === 'meditation') {
    return `Я пришла(ёл) после медитации активации дара "${dar_name || '...'}".
Я выполнила(л) практику и хочу поделиться своими ощущениями и образами, которые пришли. Веди меня бережно.`;
  }
  return `Я пришла(ёл) с квестом "Грань дара" по дару "${dar_name || '...'}".
Мне показали одну из граней: "${shadow_title || ''}".
Описание грани (чтобы ты понимал контекст): ${shadow_description || ''}.
Путь света: ${shadow_correction || ''}.
Я хочу узнать эту грань в себе и поработать с ней. Веди меня бережно.`;
}

module.exports = async (req, res) => {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type, x-telegram-init-data, x-telegram-id');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const tgUser = getUser(req);

  const body = req.body || {};
  const {
    quest_type,
    dar_name,
    shadow_title,
    shadow_description,
    shadow_correction,
    user_answer,
    gender,
    dialogue,
    round_number,
    user_action
  } = body;

  if (!user_answer || typeof user_answer !== 'string') {
    return res.status(400).json({ error: 'user_answer required' });
  }

  const trimmed = user_answer.trim();

  // Ранний отказ для очень коротких первых ответов
  const roundNum = Math.max(1, parseInt(round_number, 10) || 1);
  if (roundNum === 1 && trimmed.length < 30) {
    return res.status(200).json({
      action: 'continue',
      message: 'Это только самое начало. Поделись чуть больше: один конкретный момент из жизни, или образ, или чувство, которое приходит, когда ты об этом думаешь.'
    });
  }
  if (roundNum > 1 && trimmed.length < 5) {
    return res.status(200).json({
      action: 'continue',
      message: 'Побудь с этим вопросом чуть дольше. Даже одно слово или образ уже помогут нам двигаться дальше. Что приходит первым, когда ты слышишь мой вопрос?'
    });
  }

  // Обработка специальных действий пользователя
  if (user_action === 'ready_to_close') {
    // Пользователь подтвердил, что готов завершить сессию
    const pathWord = gender === 'male' ? 'прошёл' : gender === 'female' ? 'прошла' : 'прошёл(прошла)';
    return res.status(200).json({
      action: 'accept',
      message: `Благодарю тебя за эту работу. Ты ${pathWord} путь, который не каждый проходит. Пусть то, что открылось, останется с тобой и прорастёт в твоей жизни.`
    });
  }

  const canOfferClose = roundNum >= 3;
  const contextBlock = buildContextBlock(body);
  const systemPrompt = buildSystemPrompt(quest_type, roundNum, gender, canOfferClose);
  const messages = buildDialogueMessages(systemPrompt, dialogue, trimmed, contextBlock);

  try {
    const groq = new Groq({ apiKey: (process.env.GROQ_API_KEY || '').trim() });
    let completion;
    try {
      completion = await groq.chat.completions.create({
        messages,
        model: 'llama-3.3-70b-versatile',
        temperature: 0.75,
        max_tokens: 500
      });
    } catch (modelErr) {
      console.log('70b failed, trying 8b:', modelErr.message);
      completion = await groq.chat.completions.create({
        messages,
        model: 'llama-3.1-8b-instant',
        temperature: 0.75,
        max_tokens: 500
      });
    }

    const raw = completion.choices[0]?.message?.content || '';
    const start = raw.indexOf('{');
    const end = raw.lastIndexOf('}');
    let parsed = null;
    if (start !== -1 && end !== -1) {
      try {
        parsed = JSON.parse(raw.slice(start, end + 1));
      } catch (e) {
        console.warn('shadow-review parse error:', e.message);
      }
    }

    // Fallback если JSON не распарсился - используем весь текст как сообщение
    if (!parsed || !parsed.message) {
      parsed = {
        action: canOfferClose ? 'offer_close' : 'continue',
        message: raw.slice(0, 600) || 'Побудь с моим вопросом. Что приходит?'
      };
    }

    // Санитайзеры
    const cleanText = (s) => {
      if (!s || typeof s !== 'string') return s;
      return s
        .replace(/\u2014/g, '-')
        .replace(/\u2013/g, '-')
        .replace(/\u2026/g, '...')
        .replace(/\u00A0/g, ' ')
        .replace(/\bтен(ь|и|ью|ей|ями|ями|ям)\b/gi, 'грань')
        .replace(/\s{2,}/g, ' ')
        .trim();
    };

    const result = {
      action: parsed.action === 'offer_close' ? 'offer_close'
            : parsed.action === 'accept' ? 'accept'
            : 'continue',
      message: cleanText(String(parsed.message || '').slice(0, 1000)),
      round_number: roundNum,
      can_offer_close: canOfferClose
    };

    // Защита: если раундов мало, не позволяем AI завершать раньше времени
    if (result.action === 'accept' && roundNum < 3) {
      result.action = 'continue';
    }

    return res.status(200).json(result);
  } catch (e) {
    console.error('shadow-review error:', e.message);
    // Fallback: продолжаем диалог с нейтральным вопросом
    return res.status(200).json({
      action: 'continue',
      message: 'Я тебя слышу. Расскажи чуть больше: что самое важное в этом опыте для тебя?'
    });
  }
};

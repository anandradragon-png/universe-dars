for (const [f, m] of VERB_PAIRS) {
  if (f === m) continue;
  _f2m.set(f.toLowerCase(), m);
  _m2f.set(m.toLowerCase(), f);
}

function _replaceWithCaseAndContext(text, map) {
  if (!text || typeof text !== 'string') return text;
  let out = text;
  for (const [from, to] of map) {
    // Регистронезависимый поиск, сохраняем регистр первой буквы.
    // Контекст: либо в начале строки, либо после «ты», «Ты», «и ты», «- ты», или после знака препинания.
    // Используем lookbehind для безопасности окружения.
    // \b не работает с кириллицей в JS → используем lookahead на не-букву или конец строки.
    const re = new RegExp(
      '(^|[\\s,.!?:;—\\-«»"\\(]|\\bты\\s+)(' +
        from.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') +
      ')(?=[^а-яёА-ЯЁ]|$)',
      'gi'
    );
    out = out.replace(re, (match, prefix, word) => {
      // Сохраняем регистр: если слово начиналось с заглавной — даём заглавную замену.
      const isUpperFirst = word[0] === word[0].toUpperCase() && word[0] !== word[0].toLowerCase();
      const replaced = isUpperFirst ? to.charAt(0).toUpperCase() + to.slice(1) : to;
      return prefix + replaced;
    });
  }
  return out;
}

function normalizeGender(text, gender) {
  if (!text || typeof text !== 'string') return text;
  if (gender === 'male') {
    return _replaceWithCaseAndContext(text, _f2m);
  }
  if (gender === 'female') {
    return _replaceWithCaseAndContext(text, _m2f);
  }
  return text;  // нейтрально не трогаем
}

function deepNormalizeGender(obj, gender) {
  if (!gender || (gender !== 'male' && gender !== 'female')) return obj;
  if (obj == null) return obj;
  if (typeof obj === 'string') return normalizeGender(obj, gender);
  if (Array.isArray(obj)) return obj.map(x => deepNormalizeGender(x, gender));
  if (typeof obj === 'object') {
    const r = {};
    for (const k of Object.keys(obj)) r[k] = deepNormalizeGender(obj[k], gender);
    return r;
  }
  return obj;
}

function postprocessHumorData(data, gender) {
  try {
    const cleaned = deepClean(data);
    const dedupped = dedupTop10(cleaned);
    const stripped = stripDuplicateHeaders(dedupped);
    // Гендер-нормализатор — последним шагом, чтобы не сломали другие правки.
    return deepNormalizeGender(stripped, gender);
  } catch (e) {
    console.warn('[message-humor] postprocess failed:', e.message);
    return data;
  }
}


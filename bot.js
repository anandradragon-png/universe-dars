// ================= ОБРАБОТКА СООБЩЕНИЙ =================
bot.on('message', async (msg) => {
    const chatId = msg.chat.id;
    const text = msg.text;

    if (text === '/start') {
        bot.sendMessage(chatId, 
            "🌌 **Добро пожаловать во Вселенную Даров!**\n\n" +
            "Я помогу вам понять себя через систему 9 Полей. Отправьте мне вашу **дату рождения** (например: 23.08.1995), и я составлю ваш точный психологический портрет.", 
            { parse_mode: 'Markdown' }
        );
        return;
    }

    const dateRegex = /^(\d{1,2})\.(\d{1,2})\.(\d{4})$/;
    const match = text.match(dateRegex);

    if (match) {
        const day = parseInt(match[1]);
        const month = parseInt(match[2]);
        const year = parseInt(match[3]);

        if (day > 31 || month > 12) {
            bot.sendMessage(chatId, "Дата неверная. Пожалуйста, проверьте формат (ДД.ММ.ГГГГ).");
            return;
        }

        // ВОЗВРАЩАЕМ СТАРОЕ ПРИВЕТСТВИЕ (как просили)
        bot.sendMessage(chatId, "⏳ *Мудрый Наставник изучает вашу дату рождения...* Это займет пару секунд.", { parse_mode: 'Markdown' });

        try {
            const rawData = generateRawData(`${day}.${month}.${year}`);
            
            if (!rawData || !rawData.giftCode) {
                throw new Error("Не удалось рассчитать код дара");
            }

            const aiText = await getAIInterpretation(rawData.giftCode);

            const fullResponse = aiText;
            
            if (fullResponse.length > 4000) {
                bot.sendMessage(chatId, fullResponse.substring(0, 4000), { parse_mode: 'Markdown' });
                setTimeout(() => {
                    bot.sendMessage(chatId, fullResponse.substring(4000), { parse_mode: 'Markdown' });
                }, 1000);
            } else {
                bot.sendMessage(chatId, fullResponse, { parse_mode: 'Markdown' });
            }

        } catch (error) {
            console.error("Ошибка бота:", error);
            bot.sendMessage(chatId, "Произошла ошибка при расчете. Проверьте консоль разработчика.");
        }
    } else {
        bot.sendMessage(chatId, "Пожалуйста, отправьте дату рождения в формате ДД.ММ.ГГГГ.");
    }
});
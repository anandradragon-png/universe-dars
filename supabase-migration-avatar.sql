-- Миграция: добавление поля avatar в таблицу users
-- Запустить в SQL Editor на dashboard.supabase.com
-- =============================================
-- Раньше аватарка хранилась только в localStorage, и при сбросе
-- браузерных данных пропадала. Теперь хранится на сервере как
-- base64 data URL (макс ~700 КБ после ужатия на клиенте).

ALTER TABLE users
  ADD COLUMN IF NOT EXISTS avatar TEXT;

-- Индекс не нужен — поле читается только вместе с пользователем
